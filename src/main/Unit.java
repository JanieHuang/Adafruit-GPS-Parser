package main;

import java.util.*;

public class Unit {
	
	public String time;
	public Date date;
	public String latitude;
	public String longitude;
	
	public Unit(String time, Date date, String latitude, String longitude)
	{
		this.time = time;
		this.date = date;
		this.latitude = latitude;
		this.longitude = longitude;
	}
	
}
